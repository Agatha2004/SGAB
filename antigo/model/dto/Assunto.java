public class Assunto {
    String tag;
    int codigo;

    public Assunto(String tag, int codigo) {
        this.tag = tag;
        this.codigo = codigo;
    }

    public getTag() {
        return tag;
    }

    public setTag(String tag) {
        this.tag = tag;
    }

    public getCodigo() {
        return codigo;
    }

    public setCodigo(int codigo) {
        this.codigo = codigo;
    }
}